#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (i <= ft_strlen(s) && s[i] != c)
	{
		i++;
	}
	if (i > ft_strlen(s))
		return (NULL);
	else
		return ((char*)&s[i]);
}
